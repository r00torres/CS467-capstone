//converst the array numbers into coordinates that can be used on the board
function makePath(path, xSize, zSize)
{
  minusX = xSize/2;
  minusZ = zSize/2;

  for(var i=0; i<path.length; i++)
  {
    path[i][0] -= minusX;
    path[i][0] = 4*path[i][0]+2;
    path[i][1] -= minusZ;
    path[i][1] = 4*path[i][1]+2;
  }
  console.log(path);
}

